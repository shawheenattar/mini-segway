
// define IO pins

#define lmencpin    2                                     //  D2 is the pin for external interrupt 0
#define rmencpin    3                                     //  D3 is the pin for external interrupt 1

#define lmdirpin    7                                     //  D7  left  motor direction
#define lmpwmpin    9                                     //  D9  left  motor PWM
#define rmdirpin    8                                     //  D8  right motor direction
#define rmpwmpin   10                                     //  D10 right motor PWM

#define powerpin    7                                     //  A7  monitors battery voltage
